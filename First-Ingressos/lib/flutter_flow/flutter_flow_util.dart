extension FFJsonObject on Object {
  T? getJsonPath<T>(String path) {
    final jsonPath = JsonPath('\$.${path.replaceAll('.', '[')}]');
    final jsonData = jsonPath.read(this);
    return jsonData.isEmpty ? null : jsonData.first.value as T?;
  }
}
